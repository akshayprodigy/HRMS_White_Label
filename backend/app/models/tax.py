"""TDS / income-tax + Form 16 + Form 24Q + gratuity records.

Scope
=====
- FY-scoped TaxSlabConfig (slabs for both regimes + standard deduction +
  87A rebate + surcharge bands + cess) stored as JSON for flexibility
  across FYs without schema churn.
- FY-scoped SectionLimitConfig (80C, 80D, 80CCD(1B), HRA metro pct,
  etc.) — also JSON.
- EmployeeTaxDeclaration: per (employee, fy) regime + investments +
  HRA + previous-employer income; with verify lifecycle.
- Form16Record: per (employee, fy). Part B generated by us; Part A
  uploaded from TRACES.
- Form24QExport: per (fy, quarter) generated export with status +
  challan tracking, mirrors Part 1's StatutoryFiling pattern.
- GratuityComputation: snapshot of the formula inputs + result at the
  moment of exit (so finance can audit the historical figure later
  even if the employee's last drawn changes via correction).

Backwards-compatibility contract
================================
Pure additive — no existing table touched. No code outside this module
modifies Employee, payroll, salary_calculator, the revision module, or
the Part-1 statutory module. Generators READ finalized payroll.
"""
from datetime import datetime, date as pydate, timezone
from typing import TYPE_CHECKING, List, Optional

from sqlalchemy import (
    Boolean, DateTime, Date, Float, ForeignKey, Integer, String, Text,
    UniqueConstraint, JSON,
)
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.db.base_class import Base


if TYPE_CHECKING:
    from .user import User
    from .employee import Employee


# ----- constants ----------------------------------------------------


class TaxRegime:
    OLD = "old"
    NEW = "new"


class DeclarationStatus:
    DRAFT = "draft"
    SUBMITTED = "submitted"
    VERIFIED = "verified"
    REJECTED = "rejected"


class Form16Status:
    PENDING_PART_A = "pending_part_a"
    READY = "ready"
    ISSUED = "issued"


class Form24QStatus:
    DRAFT = "draft"
    GENERATED = "generated"
    SUBMITTED = "submitted"
    ACCEPTED = "accepted"
    REJECTED = "rejected"


class GratuityStatus:
    ACCRUED = "accrued"            # not yet payable
    ELIGIBLE = "eligible"          # eligibility met but employee active
    COMPUTED = "computed"           # at exit
    PAID = "paid"


# ----- masters ------------------------------------------------------


class TaxSlabConfig(Base):
    """One row per (FY, regime) pair OR one row per FY with both
    regimes inside `slabs_json`. We use the second shape to keep the
    UI simple: one form, two columns.

    JSON shape:
      {
        "old": [ {"upto": 250000, "rate": 0}, ..., {"upto": null, "rate": 30} ],
        "new": [ {"upto": 300000, "rate": 0}, ..., {"upto": null, "rate": 30} ],
        "surcharge_old": [ {"upto": 5000000, "rate": 0}, ... ],
        "surcharge_new": [ {"upto": 5000000, "rate": 0}, ... ],
      }
    """
    __tablename__ = "tax_slab_config"  # type: ignore[assignment]

    id: Mapped[int] = mapped_column(Integer, primary_key=True, index=True)
    fy: Mapped[str] = mapped_column(
        String(8), unique=True, nullable=False, index=True,
    )                                  # e.g. "2024-25"
    name: Mapped[str] = mapped_column(String(120), nullable=False)

    slabs_json: Mapped[dict] = mapped_column(JSON, nullable=False)

    # Standard deduction (old / new regime).
    standard_deduction_old: Mapped[float] = mapped_column(
        Float, default=50000.0, nullable=False,
    )
    standard_deduction_new: Mapped[float] = mapped_column(
        Float, default=75000.0, nullable=False,
    )

    # Section 87A rebate caps (the threshold of taxable income below
    # which rebate applies, and the max rebate amount).
    rebate_87a_old_threshold: Mapped[float] = mapped_column(
        Float, default=500000.0, nullable=False,
    )
    rebate_87a_old_max: Mapped[float] = mapped_column(
        Float, default=12500.0, nullable=False,
    )
    rebate_87a_new_threshold: Mapped[float] = mapped_column(
        Float, default=700000.0, nullable=False,
    )
    rebate_87a_new_max: Mapped[float] = mapped_column(
        Float, default=25000.0, nullable=False,
    )

    # Cess (Health + Education): 4% of (tax + surcharge).
    cess_rate: Mapped[float] = mapped_column(
        Float, default=4.0, nullable=False,
    )

    is_active: Mapped[bool] = mapped_column(Boolean, default=True, nullable=False)
    notes: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc), nullable=False,
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc),
        onupdate=lambda: datetime.now(timezone.utc), nullable=False,
    )


class SectionLimitConfig(Base):
    """One row per (FY, section_code). e.g.

      (2024-25, "80C", 150000)
      (2024-25, "80D_self_below_60", 25000)
      (2024-25, "80CCD_1B", 50000)
      (2024-25, "hra_metro_pct", 50.0)
      (2024-25, "hra_non_metro_pct", 40.0)

    Storing as a key/limit table (instead of a hard-coded enum) keeps
    new sections (e.g. 80EEB for EV loans) addable without a schema
    change.
    """
    __tablename__ = "section_limit_config"  # type: ignore[assignment]
    __table_args__ = (
        UniqueConstraint("fy", "section_code", name="uq_section_limit_fy_code"),
    )

    id: Mapped[int] = mapped_column(Integer, primary_key=True, index=True)
    fy: Mapped[str] = mapped_column(String(8), index=True, nullable=False)
    section_code: Mapped[str] = mapped_column(String(40), nullable=False)
    limit_amount: Mapped[float] = mapped_column(Float, nullable=False)
    # Some sections (e.g. HRA percentages) are not amounts; we use
    # `is_percentage` so the UI renders the field correctly.
    is_percentage: Mapped[bool] = mapped_column(
        Boolean, default=False, nullable=False,
    )
    # The regime in which the limit applies; "BOTH" by default.
    applies_to: Mapped[str] = mapped_column(
        String(8), default="BOTH", nullable=False,
    )
    notes: Mapped[Optional[str]] = mapped_column(String(255), nullable=True)
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc), nullable=False,
    )


class GratuityConfig(Base):
    """One row, occasionally updated when the cap moves (e.g. Modi
    govt raised it ₹10L → ₹20L in 2018). FY-scoped so historical
    computations replay correctly.
    """
    __tablename__ = "gratuity_config"  # type: ignore[assignment]

    id: Mapped[int] = mapped_column(Integer, primary_key=True, index=True)
    effective_from: Mapped[pydate] = mapped_column(
        Date, nullable=False, unique=True, index=True,
    )
    # Statutory ceiling (₹20,00,000 since 29-Mar-2018).
    statutory_cap: Mapped[float] = mapped_column(
        Float, default=2000000.0, nullable=False,
    )
    # Minimum continuous service for eligibility (years). The Act says 5.
    eligibility_years: Mapped[int] = mapped_column(
        Integer, default=5, nullable=False,
    )
    # Days basis: 26 working days/month (the Payment of Gratuity Act
    # formula). Some companies use 30 — make it configurable.
    days_basis: Mapped[int] = mapped_column(
        Integer, default=26, nullable=False,
    )
    is_active: Mapped[bool] = mapped_column(Boolean, default=True, nullable=False)
    notes: Mapped[Optional[str]] = mapped_column(Text, nullable=True)
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc), nullable=False,
    )


