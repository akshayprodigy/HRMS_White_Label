class EmployeeTaxDeclaration(Base):
    """Per (employee, fy) declaration.

    `declarations_json` shape (free-form, all numeric):
      {
        "80C": 150000,
        "80D": 25000,
        "80CCD_1B": 50000,
        "80E": 0,
        ...
      }
    The amount that actually counts toward exemption is min(declared,
    section_limit) — computed at projection time, not here.
    """
    __tablename__ = "employee_tax_declaration"  # type: ignore[assignment]
    __table_args__ = (
        UniqueConstraint(
            "employee_id", "fy", name="uq_emp_tax_decl_fy",
        ),
    )

    id: Mapped[int] = mapped_column(Integer, primary_key=True, index=True)
    employee_id: Mapped[int] = mapped_column(
        Integer, ForeignKey("employee.id", ondelete="CASCADE"),
        nullable=False, index=True,
    )
    fy: Mapped[str] = mapped_column(String(8), index=True, nullable=False)

    regime: Mapped[str] = mapped_column(
        String(8), default="new", nullable=False,
    )

    declarations_json: Mapped[dict] = mapped_column(
        JSON, default=dict, nullable=False,
    )

    # HRA inputs.
    monthly_rent_paid: Mapped[float] = mapped_column(
        Float, default=0.0, nullable=False,
    )
    rented_in_metro: Mapped[bool] = mapped_column(
        Boolean, default=False, nullable=False,
    )
    landlord_pan: Mapped[Optional[str]] = mapped_column(String(20), nullable=True)

    # Other income (e.g. interest income, dividends).
    other_income_annual: Mapped[float] = mapped_column(
        Float, default=0.0, nullable=False,
    )
    # Salary from previous employer in same FY (for joiners mid-year).
    previous_employer_income: Mapped[float] = mapped_column(
        Float, default=0.0, nullable=False,
    )
    previous_employer_tds: Mapped[float] = mapped_column(
        Float, default=0.0, nullable=False,
    )

    status: Mapped[str] = mapped_column(
        String(20), default=DeclarationStatus.DRAFT,
        nullable=False, index=True,
    )
    submitted_at: Mapped[Optional[datetime]] = mapped_column(
        DateTime(timezone=True), nullable=True,
    )
    verified_at: Mapped[Optional[datetime]] = mapped_column(
        DateTime(timezone=True), nullable=True,
    )
    verified_by_id: Mapped[Optional[int]] = mapped_column(
        Integer, ForeignKey("user.id", ondelete="SET NULL"), nullable=True,
    )
    rejection_reason: Mapped[Optional[str]] = mapped_column(
        String(500), nullable=True,
    )

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc), nullable=False,
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc),
        onupdate=lambda: datetime.now(timezone.utc), nullable=False,
    )


