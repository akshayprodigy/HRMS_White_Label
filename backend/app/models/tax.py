# ----- Gratuity computation snapshot --------------------------------


class GratuityComputation(Base):
    """Snapshot of a gratuity calculation. We persist these so finance
    can audit the figure later, even if the employee's last_drawn
    changes via a correction or a back-dated revision.
    """
    __tablename__ = "gratuity_computation"  # type: ignore[assignment]

    id: Mapped[int] = mapped_column(Integer, primary_key=True, index=True)
    employee_id: Mapped[int] = mapped_column(
        Integer, ForeignKey("employee.id", ondelete="CASCADE"),
        nullable=False, index=True,
    )
    config_id: Mapped[Optional[int]] = mapped_column(
        Integer, ForeignKey("gratuity_config.id", ondelete="SET NULL"),
        nullable=True,
    )
    # Optional FK to the exit Resignation row, when this is the
    # exit-time figure.
    resignation_id: Mapped[Optional[int]] = mapped_column(
        Integer, ForeignKey("resignation.id", ondelete="SET NULL"),
        nullable=True, index=True,
    )

    as_of: Mapped[pydate] = mapped_column(
        Date, nullable=False, index=True,
    )                                       # the "today" the figure
                                            # represents (last_working_day
                                            # for exits, today() for accrual)

    last_drawn_basic_da: Mapped[float] = mapped_column(
        Float, nullable=False,
    )
    raw_years: Mapped[float] = mapped_column(Float, nullable=False)
    rounded_years: Mapped[int] = mapped_column(Integer, nullable=False)
    is_eligible: Mapped[bool] = mapped_column(
        Boolean, nullable=False,
    )
    computed_amount: Mapped[float] = mapped_column(Float, nullable=False)
    capped_amount: Mapped[float] = mapped_column(Float, nullable=False)
    cap_applied: Mapped[bool] = mapped_column(
        Boolean, default=False, nullable=False,
    )

    status: Mapped[str] = mapped_column(
        String(20), default=GratuityStatus.ACCRUED,
        nullable=False, index=True,
    )

    notes: Mapped[Optional[str]] = mapped_column(String(500), nullable=True)
    computed_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        default=lambda: datetime.now(timezone.utc), nullable=False,
    )
    computed_by_id: Mapped[Optional[int]] = mapped_column(
        Integer, ForeignKey("user.id", ondelete="SET NULL"), nullable=True,
    )
